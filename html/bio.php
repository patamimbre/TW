        <p>Graba en japonés su canción <em>Como el álamo al camino</em> (Anataro Uramo) en 1971. Al año siguiente graba su primer disco en <strong>alemán</strong>, 
            en 1978 firma contrato con CBS Internacional y graba su primer disco en <strong>francés</strong>, y en <strong>italiano</strong>. En 1979 graba su primer álbum 
            en portugués. Se convirtió en el cantante que más idiomas canta: alemán, inglés, francés, italiano, portugués, español, 
            tagalog y japonés. El Libro Guinness de los Récords le entrega, en París (1983), el Disco de Diamantes por haber vendido 
            más de cien millones de discos en seis idiomas.
        </p>
        <p>En 1984 le concedieron la <strong>Estrella de la Fama</strong> siendo inmortalizado en el Hollywood Walk of Fame. En 1997 logró 
            el <strong>Premio Mónaco de La Música como el Mejor Cantante Latino</strong> y ha sido el primer artista extranjero en la historia de China 
            que ha recibido el prestigioso <strong>Golden Record Award</strong>, en 1995.
        </p>
        <p>Julio Iglesias se convirtió en uno de los artistas más populares en todo el mundo habiendo vendido <strong>300 millones</strong> de sus más de <strong>80 álbumes</strong>,
                logrando más de <strong>1000 discos de oro y 500 de platino.</strong>
        </p>
