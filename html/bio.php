        <p>Nació el 23 de septiembre de 1943 en <strong>Madrid</strong> (España).</p>
        <p> Se inició en el mundo de la fama no como cantante, sino como deportista. Jugó como <strong>portero</strong> en el equipo de fútbol del <strong>Real Madrid</strong>. 
            Quiso ser futbolista profesional mientras estudiaba <strong>Derecho</strong> en el Colegio Mayor de San Pablo.
        </p>
        <p> Cuando tenía veinte años, el 22 de septiembre de 1962, sufrió un accidente de tráfico que le dejó semiparalítico
                durante un año y medio. El enfermero que lo cuidaba le regaló una guitarra. Julio pasaba horas enteras oyendo 
                radio y escribiendo poemas. Versos tristes y románticos que preguntaban por la misión de los hombres en la vida.
        </p>
        <div class="img container center">
            <img src="http://3.bp.blogspot.com/-lOMq1r_axf0/VHvZLPy_mZI/AAAAAAAAAJU/velMEDkEqkY/s1600/Julio%2BIglesias%2BReal%2BMadrid%2BDupalu.jpg" alt="julio-portero">
        </div>
        <p>Poco a poco aprendió a tocar la guitarra, para matar el tiempo. Cuando se recuperó, comenzó de nuevo con sus estudios 
            y viajó a <strong>Londres</strong> para estudiar inglés, primero en Ramsgate y luego en el Bell's Language School en Cambrige. 
            Algunos fines de semana cantaba en un "pub", el Air Port Pub, canciones que eran populares por entonces 
            - Tom Jones, Engelbert Humperdinck, The Beatles...- y fue allí, en <strong>Cambrige</strong>, donde conoció a <strong>Gwendolyne Bollore</strong> 
            quien sería su novia y más tarde uno de sus éxitos musicales con una canción dedicada a ella, <em>Gwendolyne</em>.
        </p>
        <p>Llevó su primera canción a una casa de discos para que se la dieran a un cantante. Cuando el gerente la escuchó le dijo: 
            "¿Y por qué no la cantas tú?", y respondió: "Porque yo no soy cantante". Al final lo convencieron 
            y el 17 de julio de 1968 ganó el <strong>Festival de Benidorm</strong> con la canción <em>La vida sigue igual</em>, después firmó un contrato 
            con Discos Columbia. Al año siguiente, participó en el <strong>Festival de Brassow</strong> (Rumania) y realiza su primera gira por América; 
            canta en el <strong>Festival de Viña del Mar</strong> (Chile) y graba su primera película: La vida sigue igual. Julio Iglesias participó 
            en el <strong>Festival de Eurovisión</strong> en 1971 mientras es número uno en las listas de todo el mundo, desde México a Argentina y 
            desde España hasta Japón. Pese a no ganar el festival grabó su <em>"Gwendolyne"</em> en cuatro idiomas y ocupó el primer lugar en 
            las listas de éxitos de España y América Latina.
        </p>
        <div class="img container center">
            <img src="https://cloud10.todocoleccion.online/discos-vinilo/tc/2012/11/20/34314180.jpg" alt="gwendolyne">
